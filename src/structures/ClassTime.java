package structures;

public class ClassTime {
    private String days;
    private String time;
    private String instructor;
    private String date;
    private String location;
    private String attribute;

    public ClassTime(String days, String time, String instructor, String date, String location, String attribute){
        this.days = days;
        this.time = time;
        this.instructor = instructor;
        this.date = date;
        this.location = location;
        this.attribute = attribute;
    }

}
